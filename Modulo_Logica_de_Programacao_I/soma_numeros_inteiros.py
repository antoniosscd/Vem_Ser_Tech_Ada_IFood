# 4. Faça um programa que peça dois números e imprima a soma deles.

soma = 0 #Inicializando a variável soma

print('Soma de 2 números.')

#Solcicita ao usuário os numeros para sama.
num = int(input("Digite o primeiro múmero: "))
num2 = int(input("Digite o segundo múmero: "))

soma += num + num2 # Faz a soma dos números digitados.

print('A soma de', num,'e',num2,'é =',soma)#Mostra a soma entre os dois números digitados.
